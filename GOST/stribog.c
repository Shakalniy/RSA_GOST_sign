// Таблица умножения в поле Галуа
static uint8_t GF_MUL_TABLE[256][256];

// Инициализация таблицы умножения в поле Галуа
void init_gf_mul_table(void) {
    for (int a = 0; a < 256; a++) {
        for (int b = 0; b < 256; b++) {
            uint8_t r = 0;
            uint8_t aa = (uint8_t)a;
            int bb = b;
            for (int i = 0; i < 8; i++) {
                if (bb & 1) r ^= aa;
                uint8_t high_bit = aa & 0x80;
                aa = (aa << 1) & 0xFF;
                if (high_bit) aa ^= 0xC3;
                bb >>= 1;
            }
            GF_MUL_TABLE[a][b] = r;
        }
    }
}

// Линейное преобразование L
void l_transform(uint8_t* state, size_t block_size) {
    for (int k = 0; k < 16; k++) {
        uint8_t t = 0;
        for (size_t i = 0; i < block_size; i++) t ^= GF_MUL_TABLE[state[i]][l_vec[i % 8]];
        memmove(state, state + 1, block_size - 1);
        state[block_size - 1] = t;
    }
}

// Преобразование E
void e_transform(uint8_t* state, uint8_t* K, size_t block_size) {
    uint8_t temp[BLOCK_SIZE];
    for (int i = 0; i < 12; i++) {
        for (size_t j = 0; j < block_size; j++) temp[j] = state[j] ^ K[j];
        for (size_t j = 0; j < block_size; j++) temp[j] = S_BOX[temp[j]];
        for (size_t j = 0; j < block_size; j++) temp[j] = temp[P_TABLE[j]];
        l_transform(temp, block_size);
        memcpy(state, temp, block_size);
        for (size_t j = 0; j < block_size; j++) K[j] ^= C[i][j];
        for (size_t j = 0; j < block_size; j++) K[j] = S_BOX[K[j]];
        for (size_t j = 0; j < block_size; j++) K[j] = K[P_TABLE[j]];
        l_transform(K, block_size);
    }
}

// Функция g_N
void g_N(uint8_t* h, const uint8_t* m, const uint8_t* N, uint8_t* out, size_t block_size) {
    uint8_t K[BLOCK_SIZE];
    for (size_t i = 0; i < block_size; i++) K[i] = h[i] ^ N[i];
    for (size_t i = 0; i < block_size; i++) K[i] = S_BOX[K[i]];
    for (size_t i = 0; i < block_size; i++) K[i] = K[P_TABLE[i]];
    l_transform(K, block_size);
    uint8_t enc[BLOCK_SIZE];
    memcpy(enc, m, block_size);
    e_transform(enc, K, block_size);
    for (size_t i = 0; i < block_size; i++) out[i] = enc[i] ^ h[i] ^ m[i];
}

// Сложение по модулю 2^512 (in-place, два аргумента)
static inline void add_mod512(uint8_t* a, const uint8_t* b) {
    uint16_t carry = 0;
    for (int i = BLOCK_SIZE - 1; i >= 0; --i) {
        uint16_t sum = a[i] + b[i] + carry;
        a[i] = sum & 0xFF;
        carry = sum >> 8;
    }
}

// Финальное преобразование
void final_transformation(uint8_t* h, const uint8_t* Sigma, const uint8_t* N,
                          uint8_t* out, int bit_size) {
    uint8_t tmp[BLOCK_SIZE];
    uint8_t zero[BLOCK_SIZE] = {0};
    g_N(h, N, zero, tmp, BLOCK_SIZE);
    memcpy(h, tmp, BLOCK_SIZE);
    g_N(h, Sigma, zero, tmp, BLOCK_SIZE);
    if (bit_size == 256) memcpy(out, tmp + 32, 32);
    else memcpy(out, tmp, BLOCK_SIZE);
}

// Основная функция хеширования согласно ГОСТ 34.11—2018
void stribog_hash(const uint8_t* msg, size_t len, uint8_t* out, int bit_size) {
    static int init = 0;
    if (!init) { init_gf_mul_table(); init = 1; }
    uint8_t h[BLOCK_SIZE];
    uint8_t N[BLOCK_SIZE] = {0};
    uint8_t Sigma[BLOCK_SIZE] = {0};
    memcpy(h, (bit_size == 512) ? IV_512 : IV_256, BLOCK_SIZE);

    // Подготовка
    size_t full_blocks = len / BLOCK_SIZE;
    size_t rem = len % BLOCK_SIZE;
    uint64_t rem_bits = (uint64_t)rem * 8;
    uint8_t blk_bits[BLOCK_SIZE] = {0}; blk_bits[62] = 0x02;

    // Этап 2: обработка полных блоков
    for (size_t i = 0; i < full_blocks; ++i) {
        const uint8_t* m_block = msg + i * BLOCK_SIZE;
        uint8_t tmp[BLOCK_SIZE];
        g_N(h, m_block, N, tmp, BLOCK_SIZE);
        memcpy(h, tmp, BLOCK_SIZE);
        add_mod512(N, blk_bits);
        add_mod512(Sigma, m_block);
    }

    // Этап 3: последний блок
    uint8_t m_last[BLOCK_SIZE] = {0};
    if (rem > 0) {
        size_t byte_pos = BLOCK_SIZE - rem - 1;
        m_last[byte_pos] = 0x01;
        memcpy(m_last + byte_pos + 1, msg + full_blocks * BLOCK_SIZE, rem);
    } else {
        m_last[BLOCK_SIZE - 1] = 0x80;
    }
    uint8_t tmp[BLOCK_SIZE];
    g_N(h, m_last, N, tmp, BLOCK_SIZE);
    memcpy(h, tmp, BLOCK_SIZE);
    uint8_t len_bits_block[BLOCK_SIZE] = {0};
    for (int i = 0; i < 8; i++) len_bits_block[BLOCK_SIZE - 8 + i] = (uint8_t)(rem_bits >> ((7 - i) * 8));
    add_mod512(N, len_bits_block);
    add_mod512(Sigma, m_last);

    // Финал
    final_transformation(h, Sigma, N, out, bit_size);
}

